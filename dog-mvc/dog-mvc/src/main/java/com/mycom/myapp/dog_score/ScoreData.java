package com.mycom.myapp.dog_score;

public class ScoreData {
	private String dog_no;
	private String name;
	private int health;
	private int agility;
	private int nose_work;
	private double avg;
	private String grade;
	
	
	public ScoreData() {
		super();
	}


	public String getDog_no() {
		return dog_no;
	}


	public void setDog_no(String dog_no) {
		this.dog_no = dog_no;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getHealth() {
		return health;
	}


	public void setHealth(int health) {
		this.health = health;
	}


	public int getAgility() {
		return agility;
	}


	public void setAgility(int agility) {
		this.agility = agility;
	}


	public int getNose_work() {
		return nose_work;
	}


	public void setNose_work(int nose_work) {
		this.nose_work = nose_work;
	}


	public double getAvg() {
		return avg;
	}


	public void setAvg(double avg) {
		this.avg = avg;
	}


	public String getGrade() {
		return grade;
	}


	public void setGrade(String grade) {
		this.grade = grade;
	}

	
	
}
